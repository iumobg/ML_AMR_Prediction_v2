def analyze_kmer_databases(sample_size=None):
    """
    Extract histograms from KMC databases to calculate genome complexity.
    It identifies and exports statistical outliers (contaminated or incomplete genomes)
    using the IQR (Interquartile Range) method.
    """
    print(f"Scanning KMC databases in {KMC_OUTPUTS_DIR}...")
    
    # Find all .kmc_pre files
    db_files = list(KMC_OUTPUTS_DIR.glob("*.kmc_pre"))
    if not db_files:
        print("ERROR: No KMC databases found. Run 02_kmer_extraction.py first.")
        sys.exit(1)
        
    print(f"Found {len(db_files)} k-mer databases.")
    
    if sample_size is not None and sample_size < len(db_files):
        np.random.seed(42)
        target_files = np.random.choice(db_files, size=sample_size, replace=False)
        print(f"\nExtracting k-mer spectra from {sample_size} random genomes...")
    else:
        target_files = db_files
        print(f"\nExtracting k-mer spectra from all {len(target_files)} genomes for rigorous QC...")
    
    aggregated_hist = {}
    genome_complexities = []
    
    for db_file in tqdm(target_files, desc="Parsing KMC DBs"):
        prefix = db_file.with_suffix('')
        genome_id = db_file.stem
        hist_out = TEMP_DIR / f"{genome_id}_hist.txt"
        
        # 1. Ask KMC tools to generate a frequency histogram
        # This tells us: "How many k-mers appear exactly 1 time, 2 times, etc."
        cmd = f"{KMC_TOOLS_BIN} transform {prefix} histogram {hist_out}"
        if not run_command(cmd):
            continue
            
        try:
            # 2. Parse the histogram file mathematically
            # Format: Count_Value Number_of_Kmers
            total_unique_kmers = 0
            with open(hist_out, 'r') as f:
                for line in f:
                    parts = line.split()
                    if len(parts) == 2:
                        kmer_freq = int(parts[0])      # e.g., K-mer occurs 5 times in the genome
                        num_kmers = int(parts[1])      # e.g., 10,000 different k-mers have this frequency
                        
                        total_unique_kmers += num_kmers
                        
                        # Add to aggregated histogram
                        aggregated_hist[kmer_freq] = aggregated_hist.get(kmer_freq, 0) + num_kmers
            
            # Store complexity metric (size of the genome's k-mer space)
            genome_complexities.append({
                'Genome': genome_id,
                'Unique_Kmers': total_unique_kmers
            })
            
        finally:
            if hist_out.exists():
                hist_out.unlink()
        
        # Explicit garbage collection per file
        gc.collect()
                
    # --- IQR Outlier Detection ---
    if not genome_complexities:
        return aggregated_hist, genome_complexities, pd.DataFrame()
        
    df_comp = pd.DataFrame(genome_complexities)
    
    # Calculate IQR boundaries
    Q1 = df_comp['Unique_Kmers'].quantile(0.25)
    Q3 = df_comp['Unique_Kmers'].quantile(0.75)
    IQR = Q3 - Q1
    
    lower_bound = Q1 - 1.5 * IQR
    upper_bound = Q3 + 3.0 * IQR
    
    # Identify outliers
    outliers = df_comp[(df_comp['Unique_Kmers'] < lower_bound) | (df_comp['Unique_Kmers'] > upper_bound)].copy()
    
    outliers['Reason'] = np.where(outliers['Unique_Kmers'] > upper_bound, 
                                 'Contaminated/Oversized', 
                                 'Incomplete/Fragmented')
                                 
    print(f"\n[QC Analysis] Found {len(outliers)} statistical outliers out of {len(df_comp)} genomes.")
    print(f"  - Lower Bound (Q1 - 1.5*IQR): {lower_bound:,.0f} k-mers")
    print(f"  - Upper Bound (Q3 + 3.0*IQR): {upper_bound:,.0f} k-mers")
    
    # Export outliers to be excluded from machine learning matrix
    if len(outliers) > 0:
        outlier_file = OUTPUT_DIR / "global_qc_outliers.csv"
        outliers.to_csv(outlier_file, index=False)
        print(f"  -> Exported outlier list to: {outlier_file} (These will be excluded from ML)")
        
        print(f"\n[Outlier Genomes Excluded ({len(outliers)}):]")
        for idx, row in outliers.sort_values(by='Unique_Kmers', ascending=False).iterrows():
            reason = "Aşırı Büyük (Kontaminasyon Risk)" if row['Reason'] == 'Contaminated/Oversized' else "Aşırı Küçük (Eksik/Bozuk Genom)"
            print(f"  - {row['Genome']}.kmc_pre : {row['Unique_Kmers']:,.0f} k-mers [{reason}]")
                
    return aggregated_hist, genome_complexities, outliers


def plot_kmer_spectrum(hist_dict):
    """
    Plot the K-mer Frequency Spectrum (KFS).
    This visually shows sequencing depth, errors, and genome repetitiveness.
    """
    if not hist_dict:
        return
        
    print("Generating Average K-mer Frequency Spectrum...")
    
    # Convert to DataFrame
    df = pd.DataFrame(list(hist_dict.items()), columns=['Frequency', 'Count'])
    df = df.sort_values('Frequency')
    
    # Filter out extreme frequencies for visualization clarity
    # Typically, frequency 1 are errors, main peak is single-copy genes
    max_freq = 50
    plot_df = df[df['Frequency'] <= max_freq]
    
    plt.figure(figsize=(10, 6))
    
    # Use log-log scale to test for power-law (Zipf's Law) distribution
    ax = sns.lineplot(x='Frequency', y='Count', data=plot_df, color='#4c72b0', marker='o', linewidth=2)
    plt.xscale('log')
    plt.yscale('log')
    
    plt.title(f'K-mer Frequency Spectrum (k={K_LENGTH})', fontsize=16, pad=20)
    plt.xlabel('K-mer Multiplicity (Log Scale)', fontsize=12)
    plt.ylabel('Number of Unique K-mers (Log Scale)', fontsize=12)
    
    # Calculate PNR (Peak-to-Noise Ratio)
    error_peak_count = plot_df[plot_df['Frequency'].isin([1, 2])]['Count'].max()
    main_peak_candidates = plot_df[(plot_df['Frequency'] >= 5) & (plot_df['Frequency'] <= 50)]
    
    if not main_peak_candidates.empty and not pd.isna(error_peak_count) and error_peak_count > 0:
        main_peak_idx = main_peak_candidates['Count'].idxmax()
        main_peak_count = main_peak_candidates.loc[main_peak_idx, 'Count']
        main_peak_freq = main_peak_candidates.loc[main_peak_idx, 'Frequency']
        pnr = main_peak_count / error_peak_count
        
        # Add Annotation Box
        textstr = '\n'.join((
            f'Error Peak (freq 1-2): {error_peak_count:,.0f}',
            f'Main Peak (freq {main_peak_freq}): {main_peak_count:,.0f}',
            f'PNR: {pnr:.4f}'
        ))
        props = dict(boxstyle='round,pad=0.5', facecolor='white', alpha=0.8, edgecolor='gray')
        ax.text(0.65, 0.95, textstr, transform=ax.transAxes, fontsize=10,
                verticalalignment='top', bbox=props)
            
    sns.despine()
    plt.tight_layout()
    output_path = OUTPUT_DIR / "05_global_kmer_frequency_spectrum.png"
    plt.savefig(output_path)
    plt.close()
    print(f" -> Saved: {output_path.name}")
    
    # Explicit garbage collection
    del df, plot_df
    gc.collect()


def plot_genome_complexity(complexities, outliers_df):
    """
    Plot the distribution of unique k-mers per genome using a Dual-Pane layout.
    Panel 1: Zoomed-in view of the healthy main distribution.
    Panel 2: Full view displaying exactly how far the outliers are.
    """
    if not complexities:
        return
        
    print("\nGenerating Dual-Pane Genomic Complexity Distribution...")
    
    df = pd.DataFrame(complexities)
    # Convert to Millions for readability
    df['Unique_Kmers_M'] = df['Unique_Kmers'] / 1_000_000
    
    # Calculate IQR Boundaries
    Q1 = df['Unique_Kmers_M'].quantile(0.25)
    Q3 = df['Unique_Kmers_M'].quantile(0.75)
    IQR = Q3 - Q1
    lower_bound = Q1 - 1.5 * IQR
    upper_bound = Q3 + 3.0 * IQR
    
    # Setup Dual-Pane Figure
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 6), gridspec_kw={'width_ratios': [2, 1]})
    fig.suptitle('Genome Complexity (Unique K-mers) QC Analysis', fontsize=18, fontweight='bold', y=1.02)
    
    # --- PANEL 1: Main Distribution (Zoomed In) ---
    # Draw violin for the main dense cluster, limiting data to roughly inside the bounds
    healthy_mask = (df['Unique_Kmers_M'] >= (lower_bound - IQR)) & (df['Unique_Kmers_M'] <= (upper_bound + IQR))
    healthy_df = df[healthy_mask]
    
    sns.violinplot(ax=ax1, x=healthy_df['Unique_Kmers_M'], color="#55a868", inner="box", alpha=0.7, linewidth=1.5)
    
    # Shade the IQR "Safe" zone
    ax1.axvspan(lower_bound, upper_bound, color='green', alpha=0.1, label='Statistical Safe Zone (IQR)')
    ax1.axvline(x=lower_bound, color='darkgreen', linestyle=':', alpha=0.9, label='Lower Bound')
    ax1.axvline(x=upper_bound, color='darkgreen', linestyle=':', alpha=0.9, label='Upper Bound')
    
    ax1.set_title('Main Distribution (Zoomed In)', fontsize=14, pad=10)
    ax1.set_xlabel(f'Number of Unique K-mers (Millions) [k={K_LENGTH}]', fontsize=12)
    ax1.legend(loc='best')
    ax1.set_yticks([])
    
    # --- PANEL 2: Full Distribution with Outliers ---
    sns.boxplot(ax=ax2, x=df['Unique_Kmers_M'], width=0.3, color="#55a868", fliersize=0) # Hide default outliers
    
    if len(outliers_df) > 0:
        outliers_df_plot = outliers_df.copy()
        outliers_df_plot['Unique_Kmers_M'] = outliers_df_plot['Unique_Kmers'] / 1_000_000
        
        # Plot outliers explicitly
        sns.stripplot(ax=ax2, x=outliers_df_plot['Unique_Kmers_M'], 
                      color="red", alpha=0.8, jitter=False, size=8, marker='X', label='Removed Outliers')
    
    ax2.set_title('Full Spread with Outliers', fontsize=14, pad=10)
    ax2.set_xlabel('Unique K-mers (Millions)', fontsize=12)
    
    if len(outliers_df) > 0:
        ax2.legend(loc='lower right')
    ax2.set_yticks([])
    
    # Finalize Layout
    sns.despine(left=True)
    plt.tight_layout()
    
    output_path = OUTPUT_DIR / "06_global_genome_complexity_distribution.png"
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close('all')
    print(f" -> Saved high-res dual-pane plot to: {output_path.name}")
    
    # Explicit garbage collection
    del df, healthy_df
    if len(outliers_df) > 0:
        del outliers_df_plot
    gc.collect()


def calculate_noise_threshold(valid_genomes_list):
    """
    Calculate the document frequency spectrum and mathematically determine the optimal min_support.
    """
    if not valid_genomes_list:
        return
        
    print("\n[Calculating Global Noise Threshold (min_support)]")
    
    list_file = TEMP_DIR / "global_genome_list.txt"
    with open(list_file, 'w') as f:
        for g in valid_genomes_list:
            f.write(f"{RAW_GENOMES_DIR}/{g}.fna\n")
            
    try:
        # Run KMC to get global k-mer database
        cmd1 = f"{KMC_BIN} -k{K_LENGTH} -ci1 -cs50 -m{KMC_MEMORY_GB} -t{THREADS} -fm @{list_file} {TEMP_DIR}/global_noise_db {TEMP_DIR}"
        print("  Running KMC to extract document frequency spectrum...")
        if not run_command(cmd1):
            return
            
        cmd2 = f"{KMC_TOOLS_BIN} transform {TEMP_DIR}/global_noise_db histogram {TEMP_DIR}/global_noise_hist.txt"
        print("  Extracting histogram...")
        if not run_command(cmd2):
            return
            
        # Parse histogram
        x_vals = []
        y_vals = []
        with open(TEMP_DIR / "global_noise_hist.txt", 'r') as f:
            for line in f:
                parts = line.split()
                if len(parts) == 2:
                    kmer_freq = int(parts[0])
                    num_kmers = int(parts[1])
                    if kmer_freq <= 50:
                        x_vals.append(kmer_freq)
                        y_vals.append(num_kmers)
                        
        if not x_vals:
            print("  Failed to parse histogram.")
            return
            
        # Mathematical Elbow Calculation (Kneedle approach)
        # Convert to numpy arrays for vector math
        x_array = np.array(x_vals, dtype=float)
        y_array = np.array(y_vals, dtype=float)
        
        # Min-Max Normalization (Scale both axes to 0-1)
        x_norm = (x_array - x_array.min()) / (x_array.max() - x_array.min())
        y_norm = (y_array - y_array.min()) / (y_array.max() - y_array.min())
        
        # Line from first to last point on the NORMALIZED curve
        x1, y1 = x_norm[0], y_norm[0]
        xn, yn = x_norm[-1], y_norm[-1]
        
        # Line equation: Ax + By + C = 0
        A = yn - y1
        B = x1 - xn
        C = xn * y1 - x1 * yn
        
        max_dist = -1
        elbow_index = 0
        
        # Calculate orthogonal distance for each normalized point
        for i in range(len(x_norm)):
            dist = abs(A * x_norm[i] + B * y_norm[i] + C) / np.sqrt(A**2 + B**2)
            if dist > max_dist:
                max_dist = dist
                elbow_index = i
                
        # Map the normalized index back to the real X value
        elbow_point = x_vals[elbow_index]
                
        print(f"  -> Mathematically Calculated Noise Threshold (Elbow): {elbow_point}")
        
        # Visualization
        plt.figure(figsize=(10, 6))
        ax = sns.lineplot(x=x_vals, y=y_vals, color='#4c72b0', marker='o', linewidth=2)
        plt.yscale('log') # Semi-Log Y scale
        
        plt.axvline(x=elbow_point, color='red', linestyle='--', linewidth=2)
        
        plt.title('K-mer Document Frequency Spectrum (Prevalence)', fontsize=16, pad=20)
        plt.xlabel('Number of Genomes (X)', fontsize=12)
        plt.ylabel('Number of Unique K-mers (Y, Log Scale)', fontsize=12)
        
        # Annotate
        textstr = f"Calculated Noise Threshold (min_support): {elbow_point}\n(Maximum Distance to Line Method)"
        props = dict(boxstyle='round,pad=0.5', facecolor='white', alpha=0.8, edgecolor='red')
        ax.text(0.5, 0.9, textstr, transform=ax.transAxes, fontsize=12,
                verticalalignment='top', horizontalalignment='center', bbox=props)
                
        sns.despine()
        plt.tight_layout()
        output_path = OUTPUT_DIR / "07_global_noise_threshold.png"
        plt.savefig(output_path)
        plt.close()
        print(f"  -> Saved elbow plot to: {output_path.name}")
        
    finally:
        # Cleanup
        if list_file.exists():
            list_file.unlink()
        for ext in ['.kmc_pre', '.kmc_suf']:
            db_file = TEMP_DIR / f"global_noise_db{ext}"
            if db_file.exists():
                db_file.unlink()
        hist_file = TEMP_DIR / "global_noise_hist.txt"
        if hist_file.exists():
            hist_file.unlink()



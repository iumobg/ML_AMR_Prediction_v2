def run_command(command):
    """Execute shell command with silent output."""
    try:
        import shlex
        subprocess.run(
            shlex.split(command),
            check=True
        )
    except subprocess.CalledProcessError as e:
        print(f"Error executing: {command}")
        return False
    return True


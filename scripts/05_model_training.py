def load_optimized_hyperparameters():
    """  
    Load optimized hyperparameters and data split from antibiotic-specific config file.
    
    This function reads the best hyperparameters and data split information from
    the antibiotic-specific configuration file generated by 04_optimization.py.
    This decoupled approach enables:
    - Parallel optimization for multiple antibiotics without conflicts
    - Reproducible training (exact same files as optimization)
    - Stratified data splits maintained across pipeline stages
    
    File Structure:
        config/config_{antibiotic}.yaml contains:
        - antibiotic_metadata: Optimization metadata (date, AUC, trials)
        - data_split: Train/test file lists (stratified split)
        - xgboost_params: Fixed base parameters
        - best_params: Optimized hyperparameters from Optuna
    
    Returns:
        tuple: (final_params, train_filenames, test_filenames)
            - final_params: Dictionary of complete XGBoost parameters
            - train_filenames: List of training chunk filenames (strings)
            - test_filenames: List of test chunk filenames (strings)
    
    Raises:
        FileNotFoundError: If antibiotic-specific config doesn't exist
        KeyError: If required sections are missing
        yaml.YAMLError: If the YAML file is malformed
    """
    # Define antibiotic-specific config path
    antibiotic_config_path = CONFIG_DIR / f"config_{TARGET_ANTIBIOTIC}.yaml"
    
    if not antibiotic_config_path.exists():
        raise FileNotFoundError(
            f"Antibiotic-specific configuration not found: {antibiotic_config_path}\n\n"
            f"This file is generated by hyperparameter optimization.\n"
            f"Please run optimization for '{TARGET_ANTIBIOTIC}' first:\n\n"
            f"  python scripts/04_optimization.py\n\n"
            f"This will create: config/config_{TARGET_ANTIBIOTIC}.yaml\n"
            f"containing optimized hyperparameters and data split for this antibiotic."
        )
    
    try:
        with open(antibiotic_config_path, 'r', encoding='utf-8') as f:
            antibiotic_config = yaml.safe_load(f)
        
        # Validate required sections
        required_sections = ['antibiotic_metadata', 'data_split', 'xgboost_params', 'best_params']
        missing_sections = [s for s in required_sections if s not in antibiotic_config]
        
        if missing_sections:
            raise KeyError(
                f"Missing sections in {antibiotic_config_path.name}: {missing_sections}\n"
                f"The config file may be corrupted. Re-run optimization:\n"
                f"  python scripts/04_optimization.py"
            )
        
        metadata = antibiotic_config['antibiotic_metadata']
        data_split = antibiotic_config['data_split']
        base_params = antibiotic_config['xgboost_params']
        optimized_params = antibiotic_config['best_params']
        
        print(f"✓ Loaded config: {antibiotic_config_path.name}")
        print("\nOptimization Metadata:")
        print("-" * 60)
        print(f"  Antibiotic       : {metadata.get('target_antibiotic', 'N/A')}")
        print(f"  Optimization Date: {metadata.get('optimization_date', 'N/A')}")
        print(f"  Best ROC AUC     : {metadata.get('best_auc_score', 'N/A'):.4f}")
        print(f"  Trials Completed : {metadata.get('n_trials_completed', 'N/A')}")
        print("-" * 60)
        
        print("\nData Split Information:")
        print("-" * 60)
        print(f"  Split Method     : {data_split.get('split_method', 'N/A')}")
        print(f"  Training Chunks  : {data_split.get('train_count', 0)}")
        print(f"  Test Chunks      : {data_split.get('test_count', 0)}")
        print("-" * 60)
        
        print("\nOptimized Hyperparameters:")
        print("-" * 60)
        for key, value in optimized_params.items():
            print(f"  {key:<20} : {value}")
        print("-" * 60)
        
        # Merge: start with global BASE_PARAMS, override with file's base_params, then optimized_params
        final_params = BASE_PARAMS.copy()
        final_params.update(base_params)
        final_params.update(optimized_params)
        
        # Extract file lists
        train_files = data_split.get('train_files', [])
        test_files = data_split.get('test_files', [])
        
        if not train_files:
            raise ValueError(
                "No training files found in data_split section.\n"
                "Re-run optimization: python scripts/04_optimization.py"
            )
        
        return final_params, train_files, test_files
        
    except yaml.YAMLError as e:
        print(f"ERROR: Malformed YAML file: {antibiotic_config_path}")
        print(f"  Details: {e}")
        sys.exit(1)
    except KeyError as e:
        print(f"ERROR: {e}")
        sys.exit(1)
    except Exception as e:
        print(f"ERROR: Failed to load hyperparameters: {e}")
        sys.exit(1)


def get_y_chunk(y_all, chunk_id, chunk_size, total_len):
    """
    Extract label subset for a specific chunk.
    
    Args:
        y_all: Complete label array
        chunk_id: Chunk identifier
        chunk_size: Samples per chunk
        total_len: Total number of samples
    
    Returns:
        Label subset for the chunk
    """
    start = chunk_id * chunk_size
    end = min((chunk_id + 1) * chunk_size, total_len)
    return y_all[start:end]




"""
Final Model Training Module for AMR Prediction

This script trains the final XGBoost model using pre-optimized hyperparameters
for antibiotic resistance prediction. Implements incremental learning for
large-scale genomic data processing with threshold optimization.

Configuration Architecture:
    This script uses a two-stage configuration loading system:
    1. Global Config (config.yaml): Project settings, paths, and target antibiotic
    2. Specific Config (config_{antibiotic}.yaml): Optimized hyperparameters and
       exact train/test split from optimization phase (04_optimization.py)

Workflow:
    1. Load global config to get paths and target antibiotic
    2. Load antibiotic-specific config for hyperparameters and data split
    3. Train model using exact training files from stratified split
    4. Evaluate on exact test files from stratified split
    5. Save trained model to antibiotic-specific directory
"""


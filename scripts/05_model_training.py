def main():
    """
    Execute complete training and evaluation pipeline.
    
    Steps:
        1. Load optimized hyperparameters and data split from antibiotic-specific config
        2. Load labels and resolve chunk file paths
        3. Use stratified train/test split from optimization (NO randomization)
        4. Train model incrementally on exact training chunks
        5. Evaluate on exact test chunks (same as optimization)
        6. Save trained model
    """
    print("=" * 80)
    print(f"XGBoost TRAINING - {TARGET_ANTIBIOTIC.upper()} RESISTANCE PREDICTION")
    print("=" * 80)
    
    # Load optimized hyperparameters and data split
    print("\n[1/5] Loading optimized hyperparameters and data split...")
    print("=" * 80)
    
    try:
        best_params, train_filenames, test_filenames = load_optimized_hyperparameters()
        print("=" * 80)
    except FileNotFoundError as e:
        print(f"ERROR: {e}")
        sys.exit(1)
    
    # Load data
    print("\n[2/5] Loading labels and discovering chunk files...")
    
    try:
        y_path = MATRIX_DIR / f"y_{TARGET_ANTIBIOTIC}.csv"
        if not y_path.exists():
            raise FileNotFoundError(f"Label file not found: {y_path}")
        
        y_all = pd.read_csv(y_path, encoding='utf-8')['label'].values
        
        all_files = list(MATRIX_DIR.glob(f"X_{TARGET_ANTIBIOTIC}_part_*.npz"))
        if not all_files:
            raise FileNotFoundError("No matrix chunks found")
        
        print(f"  ✓ Loaded {len(y_all)} labels, {len(all_files)} chunks")
        
    except FileNotFoundError as e:
        print(f"ERROR: {e}")
        sys.exit(1)
    
    # Use train/test split from config (reproducible, stratified)
    print("\n[3/5] Loading stratified train/test split from config...")
    
    # Load antibiotic-specific config to show optuna subset info
    try:
        antibiotic_config_path = CONFIG_DIR / f"config_{TARGET_ANTIBIOTIC}.yaml"
        with open(antibiotic_config_path, 'r') as f:
            antibiotic_config = yaml.safe_load(f)
        optuna_count = antibiotic_config.get('data_split', {}).get('optuna_count', 'N/A')
    except Exception:
        optuna_count = 'N/A'
    
    # Create filename -> path mapping
    file_map = {f.name: f for f in all_files}
    
    # Resolve train files from config
    train_files = []
    for fname in train_filenames:
        if fname in file_map:
            train_files.append(file_map[fname])
        else:
            print(f"WARNING: Training file '{fname}' not found in matrix directory")
    
    # Resolve test files from config
    test_files = []
    for fname in test_filenames:
        if fname in file_map:
            test_files.append(file_map[fname])
        else:
            print(f"WARNING: Test file '{fname}' not found in matrix directory")
    
    if not train_files:
        print("ERROR: No training files resolved from config")
        sys.exit(1)
    
    if not test_files:
        print("ERROR: No test files resolved from config")
        sys.exit(1)
    
    print(f"  ✓ Training: {len(train_files)} chunks (FULL Training Set)")
    print(f"  ✓ Test: {len(test_files)} chunks (Stratified Test Set)")
    print(f"  ✓ Optuna used: {optuna_count} chunks (hyperparameter tuning only)")
    print("  Note: Training on FULL set, not the optuna subset")
    
    # Train model and calculate unbiased threshold from training prevalence
    print("\n[4/5] Training model...")
    final_model, optimal_threshold = final_training_incremental(best_params.copy(), train_files, y_all)
    
    # Evaluate without test set leakage
    print("\n[5/5] Evaluating model...")
    final_test(final_model, test_files, y_all, optimal_threshold)
    
    # Save model
    model_path = MODELS_DIR / f"xgboost_{TARGET_ANTIBIOTIC}_final_v2.json"
    
    try:
        final_model.save_model(str(model_path))
        print(f"\n✓ Model saved: {model_path}")
        
        # Save threshold to the antibiotic-specific config
        antibiotic_config['evaluation'] = {
            'optimal_threshold': float(round(float(optimal_threshold), 4)),
            'threshold_type': 'Balanced_by_Scale_Pos_Weight (0.5)'
        }
        
        with open(antibiotic_config_path, 'w', encoding='utf-8') as f:
            yaml.dump(antibiotic_config, f, default_flow_style=False, sort_keys=False)
            
        print(f"✓ Unbiased Threshold saved to config: {antibiotic_config_path.name}")
        
    except Exception as e:
        print(f"ERROR: Failed to save model or config: {e}")
        sys.exit(1)
    
    print("\n" + "=" * 80)
    print("TRAINING COMPLETE")
    print("=" * 80)


# ============================================================================
# SCRIPT ENTRY POINT
# ============================================================================
if __name__ == "__main__":
    main()

